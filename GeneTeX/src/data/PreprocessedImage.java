package data;

public class PreprocessedImage {
	
    /* ************************************************************************
     *                               ATTRIBUTES                               * 
     ************************************************************************ */
	
	private boolean[][] pixels;
	
    /* ************************************************************************
     *                              CONSTRUCTORS                              * 
     ************************************************************************ */
	
	public PreprocessedImage(boolean[][] pixels) {
		this.pixels = pixels;
	}
	
    /* ************************************************************************
     *                              GETTERS                                   * 
     ************************************************************************ */
	
	public boolean[][] getPixels() {
		return pixels;
	}

}
